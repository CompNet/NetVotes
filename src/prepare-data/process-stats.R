		data <- data / sum(data)
		data[is.na(data)] <- 0		# remove possible /0 outcomes
		data <- data.frame(data)
		data <- cbind(data.frame(GROUP.VALUES),data)
		colnames(data) <- c(COL.GROUP, COL.COUNT)
		table.file <- paste(plot.file,".csv",sep="")
